const _originalTextNode = new WeakMap<Text, string>();
const ATTRS_TO_TRANSLATE = ['placeholder', 'title', 'aria-label'] as const;

function translateValue(source: string, lang: AppLanguage): string {
  if (lang === 'ca') return source;

  const exact = EXACT_TRANSLATIONS[lang][source];
  if (exact) return exact;

  let value = source;
  for (const [pattern, replacement] of TEXT_REPLACEMENTS[lang]) {
    value = value.replace(pattern, replacement);
  }
  return value;
}

function shouldSkipTextNode(node: Text): boolean {
  const parent = node.parentElement;
  if (!parent) return true;
  const tag = parent.tagName;
  if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT' || tag === 'TEXTAREA') return true;
  return false;
}

function translateTextNodes(root: ParentNode, lang: AppLanguage) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  let current = walker.nextNode();
  while (current) {
    const textNode = current as Text;
    if (!shouldSkipTextNode(textNode)) {
      const currentText = textNode.nodeValue || '';
      if (currentText.trim()) {
        const original = _originalTextNode.get(textNode) ?? currentText;
        _originalTextNode.set(textNode, original);
        const translated = translateValue(original, lang);
        if (translated !== currentText) {
          textNode.nodeValue = translated;
        }
      }
    }
    current = walker.nextNode();
  }
}

function translateAttributes(root: ParentNode, lang: AppLanguage) {
  const elements = root instanceof Element ? [root, ...Array.from(root.querySelectorAll('*'))] : Array.from(root.querySelectorAll('*'));
  for (const el of elements) {
    for (const attr of ATTRS_TO_TRANSLATE) {
      const current = el.getAttribute(attr);
      if (!current) continue;

      const dataKey = `data-i18n-orig-${attr.replace('-', '_')}`;
      const original = el.getAttribute(dataKey) || current;
      if (!el.getAttribute(dataKey)) {
        el.setAttribute(dataKey, original);
      }
      const translated = translateValue(original, lang);
      if (translated !== current) {
        el.setAttribute(attr, translated);
      }
    }
  }
}

export function applyLanguageToDocument(lang: AppLanguage, root?: ParentNode) {
  if (typeof document === 'undefined') return;
  const target = root || document.body;
  translateTextNodes(target, lang);
  translateAttributes(target, lang);
}

export function normalizeLang(value: unknown): AppLanguage {
  if (value === 'en' || value === 'es') return value;
  return 'ca';
}

export function getStoredLang(): AppLanguage {
  if (typeof window === 'undefined') return 'ca';
  return normalizeLang(localStorage.getItem(APP_LANG_KEY));
}

export function setStoredLang(lang: AppLanguage) {
  if (typeof window === 'undefined') return;
  localStorage.setItem(APP_LANG_KEY, lang);
}
